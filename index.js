var miVariable = 'Mi variable'

console.log(miVariable)

/*
    La diferencia entre var y let es que
        var: Se crea y declara la variable desde el comienzo del código.
        let: Se crea y declara la variable desde la línea en donde se creó.
    
    Si se trata de mostrar el resultado con console.log(), si se usa let se tien que
    poner despues de la línea escrita, si se pone antes dará error ya que js no lo detecta antes porque no es
    tipo var.

        const: No se puede cambiar el valor por ningun motivo y si se quiere mostrar debe ser despues
        de escribir la línea (como en el caso de let)
*/